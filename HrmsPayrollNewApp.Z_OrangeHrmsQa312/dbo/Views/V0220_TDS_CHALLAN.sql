


CREATE VIEW [dbo].[V0220_TDS_CHALLAN]
AS
SELECT     Tc.*,Additional_Amount,(TC.Tax_Amount - Challan_Detail.Additional_Amount) as TaxAmt
FROM         dbo.T0220_TDS_CHALLAN TC WITH (NOLOCK) LEFT OUTER JOIN
		(SELECT SUM(TD.Additional_Amount) As Additional_Amount,T.Challan_Id FROM 
		T0220_TDS_CHALLAN T WITH (NOLOCK)  INNER JOIN 
		T0230_TDS_CHALLAN_DETAIL TD WITH (NOLOCK)  ON T.Challan_Id = TD.Challan_Id		
		GROUP BY T.Challan_Id,T.Month,T.Year) Challan_Detail ON TC.Challan_Id = Challan_Detail.Challan_Id

GO
