CREATE VIEW [dbo].[V0010_Emp_Gallery]
AS
SELECT     Gallery_ID, Type, Purpose, Name, Cmp_ID, Upload_By, Upload_Date, Emp_Id_Multi, CASE WHEN LEN(Emp_Code_Multi) > 50 THEN LEFT(replace(Emp_Code_Multi, '#', ';'), 50) 
                      + '...' ELSE replace(Emp_Code_Multi, '#', ';') END AS Emp_Code_Multi, REPLACE(Emp_Code_Multi, '#', ';') AS New_Emp_Code_Multi, Gallery_Name, expiry_Date, CASE WHEN charindex('#', Name) 
                      > 0 THEN CASE WHEN Type = 'Images' THEN 'Images/' + SUBSTRING(LEFT(Name, CHARINDEX('#', Name) - 1), 0, LEN(LEFT(Name, CHARINDEX('#', Name)))) 
                      ELSE 'Images/notavailable.png' END ELSE CASE WHEN Type = 'Images' THEN 'Images/' + Name ELSE 'Images/notavailable.png' END END AS FirstImgPath
FROM         dbo.T0010_Emp_Gallery WITH (NOLOCK)


