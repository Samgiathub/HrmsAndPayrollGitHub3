


CREATE VIEW [dbo].[V0150_EMP_INOUT_RECORD]  
AS  
SELECT     EIR.For_Date, MIN(In_Time) AS In_Time, Case When Max_In_Date > MAX(Out_Time) Then Max_In_Date Else MAX(Out_Time) End AS Out_Time, Cmp_ID, 
		EIR.Emp_ID, dbo.F_Return_Hours(DATEDIFF(s, MIN(In_Time), Case When Max_In_Date > MAX(Out_Time) Then Max_In_Date Else MAX(Out_Time) End))   
                      AS Duration, App_Date  
FROM         dbo.T0150_EMP_INOUT_RECORD EIR WITH (NOLOCK) Inner Join
(select Emp_Id, Max(In_Time) Max_In_Date,For_Date From T0150_Emp_Inout_Record WITH (NOLOCK) Group By Emp_Id,For_Date) Q4 on EIR.Emp_Id = Q4.Emp_Id  
And EIR.For_Date = Q4.For_Date
GROUP BY EIR.For_Date, EIR.Emp_ID, Cmp_ID, App_Date ,Max_In_Date 


GO
