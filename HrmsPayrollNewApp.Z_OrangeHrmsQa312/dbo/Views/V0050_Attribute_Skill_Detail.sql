



CREATE VIEW [dbo].[V0050_Attribute_Skill_Detail]
AS
SELECT     CONVERT(varchar(50), dbo.T0100_EMP_SKILL_ATTR_ASSIGN.Effect_Date, 103) AS Effect_Date, dbo.T0100_EMP_SKILL_ATTR_ASSIGN.Skill_Weightage, 
                      dbo.T0100_EMP_SKILL_ATTR_ASSIGN.Attr_Weightage, dbo.T0040_DESIGNATION_MASTER.Desig_Name, dbo.T0100_EMP_SKILL_ATTR_ASSIGN.Desig_Id, 
                      dbo.T0100_EMP_SKILL_ATTR_ASSIGN.Cmp_ID, dbo.T0100_EMP_SKILL_ATTR_ASSIGN.Tran_ID, dbo.T0100_EMP_SKILL_ATTR_ASSIGN.Type,Dept_Name,dbo.T0100_EMP_SKILL_ATTR_ASSIGN.Dept_Id
FROM         dbo.T0100_EMP_SKILL_ATTR_ASSIGN WITH (NOLOCK)
		LEFT JOIN    dbo.T0040_DESIGNATION_MASTER WITH (NOLOCK) ON dbo.T0040_DESIGNATION_MASTER.Desig_ID = dbo.T0100_EMP_SKILL_ATTR_ASSIGN.Desig_Id 
         LEFT JOIN   dbo.T0040_DEPARTMENT_MASTER WITH (NOLOCK) ON dbo.T0040_DEPARTMENT_MASTER.Dept_ID = dbo.T0100_EMP_SKILL_ATTR_ASSIGN.Dept_ID


