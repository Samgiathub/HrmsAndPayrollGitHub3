




CREATE VIEW [dbo].[GET_EMPLOYEE_TASK]
AS
SELECT Emp_ID,(ISNULL(Emp_First_Name,'')+''+ISNULL(Emp_Last_Name,''))AS 'EmployeeName',DM.Desig_Name,BM.Branch_Name,EM.Cmp_ID 
FROM T0080_EMP_MASTER EM WITH (NOLOCK)
INNER JOIN T0030_BRANCH_MASTER BM WITH (NOLOCK) ON EM.Branch_ID = BM.Branch_ID 
INNER JOIN T0040_DESIGNATION_MASTER DM WITH (NOLOCK) ON EM.Desig_Id = DM.Desig_ID 

