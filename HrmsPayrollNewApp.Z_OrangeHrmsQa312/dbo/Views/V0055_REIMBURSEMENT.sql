





CREATE VIEW [dbo].[V0055_REIMBURSEMENT]
AS
SELECT     R.RIMB_ID, R.CMP_ID, R.RIMB_NAME, R.RIMB_FLAG, R.RIMB_LEVEL, R.AD_ID, AM.AD_NAME
FROM         dbo.T0055_REIMBURSEMENT AS R WITH (NOLOCK) LEFT OUTER JOIN
                      dbo.T0050_AD_MASTER AS AM  WITH (NOLOCK) ON R.AD_ID = AM.AD_ID




